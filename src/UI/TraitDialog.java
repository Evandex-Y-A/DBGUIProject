    /**
     * Handles the "Add" button click.
     * <p>
     * Disables buttons, launches a background task to check for duplicates and
     * add the trait via {@link TraitDAO}, and then re-enables buttons and
     * shows either a confirmation or an error message.
     * </p>
     *
     * @param evt the action event (ignored)
     */
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        addButton.setEnabled(false);
        doneButton.setEnabled(false);
        new SwingWorker<Boolean, Void>() {
            private String trait = traitField.getText();
            private String errorMsg = null;

            @Override
            protected Boolean doInBackground() {
                TraitDAO traitAdder = new TraitDAO();
                try {
                    if (traitAdder.checkTrait(trait)) {
                        errorMsg = "Trait already exists. Please enter a different trait.";
                        return false;
                        }
                    boolean created = traitAdder.addTrait(trait);
                    if (!created) errorMsg = "Failed to add trait.";
                    return created;
                } catch (Exception ex) {
                    errorMsg = "Database error: " + ex.getMessage();
                    ex.printStackTrace();
                    return false;
                } finally {
                    traitAdder.closeConnection();
                }
            }
            @Override
            protected void done() {
                addButton.setEnabled(true);
                doneButton.setEnabled(true);
                
                boolean success = false;
                try {
                    success = get();
                } catch (Exception ex) {
                    errorMsg = "Unexpected error: " + ex.getMessage();
                }

                if (success && !disableConfirmation) {
                    JOptionPane.showMessageDialog(
                        TraitDialog.this,
                        "Trait successfully added!",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE
                    );
                } else if (!success) {
                    JOptionPane.showMessageDialog(
                        TraitDialog.this,
                        errorMsg,
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                    );
                }
            }
        }.execute();
        
    }//GEN-LAST:event_addButtonActionPerformed

